    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String maSV = txtMaSV.getText();
        String hoTen = txtHoten.getText();
        String email = txtEmail.getText();
        String sdt = txtSDT.getText();
        String diaChi = txtaDiaChi.getText();
        boolean gioiTinh;
        if (chkNam.isSelected() == true) {
            gioiTinh = true;
        } else {
            gioiTinh = false;
        }
        String hinh = strHinhAnh;
        STUDENTS stu = new STUDENTS(maSV, hoTen, email, sdt, gioiTinh, diaChi, hinh);
        String result = gss.deleteGrade(maSV);
        result = sss.removeStudent(stu);
        loadTable();
        JOptionPane.showMessageDialog(this, result);
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int flag = 0;
        String maSV = txtMaSV.getText();
        String hoTen = txtHoten.getText();
        String email = txtEmail.getText();
        String sdt = txtSDT.getText();
        String diaChi = txtaDiaChi.getText();
        boolean gioiTinh;
        if (chkNam.isSelected() == true) {
            gioiTinh = true;
        } else {
            gioiTinh = false;
        }
        String hinh = strHinhAnh;
        if (maSV.equals("") || hoTen.equals("") || email.equals("") || sdt.equals("") || diaChi.equals("")) {
            JOptionPane.showMessageDialog(this, "Các thông tin phải điền đầy đủ");
        } else {
            for (STUDENTS o : listST) {
                if (o.getMaSV().equals(maSV)) {
                    flag = 0;
                    break;
                }
                flag = 1;
            }
            if (flag == 1){
                JOptionPane.showMessageDialog(this, "Mã sinh viên không tồn tại");
            }

            if (!email.matches("\\w+@\\w+\\.\\w+")) {
                JOptionPane.showMessageDialog(this, "email không đúng định dạng ");
                flag = 1;
            }

            if (!sdt.matches("^0[0-9]{9}$")) {
                JOptionPane.showMessageDialog(this, "Số điện thoại sai định dạng");
                flag = 1;
            }

            if (flag == 0) {
                STUDENTS stu = new STUDENTS(maSV, hoTen, email, sdt, gioiTinh, diaChi, hinh);
                String result = sss.updateStudent(stu);
                loadTable();
                JOptionPane.showMessageDialog(this, result);
            }
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        int flag = 0;
        String maSV = txtMaSV.getText();
        String hoTen = txtHoten.getText();
        String email = txtEmail.getText();
        String sdt = txtSDT.getText();
        String diaChi = txtaDiaChi.getText();
        String hinh;
        boolean gioiTinh;
        if (chkNam.isSelected() == true) {
            gioiTinh = true;
        } else {
            gioiTinh = false;
        }

        if (strHinhAnh == null) {
            hinh = "null";
        } else {
            hinh = strHinhAnh;
        }

        if (maSV.equals("") || hoTen.equals("") || email.equals("") || sdt.equals("") || diaChi.equals("")) {
            JOptionPane.showMessageDialog(this, "Các thông tin phải điền đầy đủ");
        } else {
            for (STUDENTS o : listST) {
                if (o.getMaSV().equals(maSV)) {
                    JOptionPane.showMessageDialog(this, "Mã sinh viên đã tồn tại");
                    flag = 1;
                    break;
                }
            }

            if (!email.matches("\\w+@\\w+\\.\\w+")) {
                JOptionPane.showMessageDialog(this, "email không đúng định dạng ");
                flag = 1;
            }

            if (!sdt.matches("^0[0-9]{9}$")) {
                JOptionPane.showMessageDialog(this, "Số điện thoại sai định dạng");
                flag = 1;
            }

            if (flag == 0) {
                STUDENTS stu = new STUDENTS(maSV, hoTen, email, sdt, gioiTinh, diaChi, hinh);
                String result = sss.addStudent(stu);
                result = gss.addGrade(maSV);
                loadTable();
                JOptionPane.showMessageDialog(this, result);
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

