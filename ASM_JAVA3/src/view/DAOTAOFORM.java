    private void loadTable() {
        listST.removeAll(listST);
        listST = sss.getListStudents();
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new String[]{"Mã SV", "Họ tên", "Email", "SDT", "Giới tính", "Địa chỉ", "Hình"});
        for (STUDENTS o : listST) {
            model.addRow(new Object[]{o.getMaSV(), o.getHoTen(), o.getEmail(), o.getSdt(), o.isGioiTinh(), o.getDiaChi(), o.getHinh()});
        }
        tblSinhVien.setModel(model);

        if (tblSinhVien.getRowCount() > 0) {
            tblSinhVien.setRowSelectionInterval(0, 0);
            loadSelected(0);
        }
    }

    private void loadSelected(int index) {
        txtMaSV.setText(listST.get(index).getMaSV());
        txtHoten.setText(listST.get(index).getHoTen());
        txtEmail.setText(listST.get(index).getEmail());
        txtSDT.setText(listST.get(index).getSdt());
        if (listST.get(index).isGioiTinh() == true) {
            chkNam.setSelected(true);
        } else {
            chkNu.setSelected(true);
        }
        txtaDiaChi.setText(listST.get(index).getDiaChi());
        if (listST.get(index).getHinh().equals("null")) {
            lblAnh.setText("null");
            lblAnh.setIcon(null);
        } else {
            lblAnh.setText("");
            ImageIcon imgIcon = new ImageIcon(getClass().getResource("/imageSrc/" + listST.get(index).getHinh()));
            Image img = imgIcon.getImage();
            img.getScaledInstance(lblAnh.getWidth(), lblAnh.getY(), 0);
            lblAnh.setIcon(imgIcon);
        }
    }

