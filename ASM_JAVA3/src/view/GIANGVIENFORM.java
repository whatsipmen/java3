    private void loadSortTable() {
        top3.removeAll(top3);
        top3 = gsv.sortGrade();
        String hoTen;
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new String[]{"Mã SV", "Họ tên", "Tiếng anh", "Tin học", "GDTC"});
        for (GRADES o : top3) {
            hoTen = sss.getNameStudent(o.getMaSV());
            model.addRow(new Object[]{o.getMaSV(), hoTen, o.getTiengAnh(), o.getTinHoc(), o.getGdTC()});
        }
        tblDiem.setModel(model);
        if (tblDiem.getRowCount() > 0) {
            loadSelected(0);
            tblDiem.setRowSelectionInterval(0, 0);
        }

    }

    private void loadTable() {
        listGR.removeAll(listGR);
        listGR = gsv.getListGrade();
        String hoTen;
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new String[]{"Mã SV", "Họ tên", "Tiếng anh", "Tin học", "GDTC"});
        for (GRADES o : listGR) {
            hoTen = sss.getNameStudent(o.getMaSV());
            model.addRow(new Object[]{o.getMaSV(), hoTen, o.getTiengAnh(), o.getTinHoc(), o.getGdTC()});
        }
        tblDiem.setModel(model);
        if (tblDiem.getRowCount() > 0) {
            loadSelected(0);
            tblDiem.setRowSelectionInterval(0, 0);
        }

    }

    private void loadSelected(int index) {
        String hoTen = sss.getNameStudent(top3.get(index).getMaSV());
        lblHoTen.setText(hoTen);
        txtMaSV.setText(top3.get(index).getMaSV());
        txtTiengAnh.setText(String.valueOf(top3.get(index).getTiengAnh()));
        txtTinHoc.setText(String.valueOf(top3.get(index).getTinHoc()));
        txtGDTC.setText(String.valueOf(top3.get(index).getGdTC()));
        lblDTB.setText(String.valueOf((top3.get(index).getTiengAnh() + top3.get(index).getTinHoc() + top3.get(index).getGdTC()) / 3));
        tblDiem.setRowSelectionInterval(index, index);
    }
