    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        listST = sss.getListStudents();
        listGR = gsv.getListGrade();
        String maSV = txtMaSV.getText();
        String ta = txtTiengAnh.getText();
        String th = txtTinHoc.getText();
        String gdtc = txtGDTC.getText();

        int flag = 0;

        if (ta.equals("")) {
            JOptionPane.showMessageDialog(this, "Điểm tiếng anh trống");
            flag = 1;
        } else if (Integer.parseInt(ta) < 0 || Integer.parseInt(ta) > 10) {
            JOptionPane.showMessageDialog(this, "Điểm phải nằm trong khoảng 0-10");
            flag = 1;
        }

        if (th.equals("")) {
            JOptionPane.showMessageDialog(this, "Điểm tiếng anh trống");
            flag = 1;
        } else if (Integer.parseInt(th) < 0 || Integer.parseInt(th) > 10) {
            JOptionPane.showMessageDialog(this, "Điểm phải nằm trong khoảng 0-10");
            flag = 1;
        }

        if (gdtc.equals("")) {
            JOptionPane.showMessageDialog(this, "Điểm tiếng anh trống");
            flag = 1;
        } else if (Integer.parseInt(gdtc) < 0 || Integer.parseInt(gdtc) > 10) {
            JOptionPane.showMessageDialog(this, "Điểm phải nằm trong khoảng 0-10");
            flag = 1;
        }

        if (flag == 0) {
            for (STUDENTS st : listST) {
                if (st.getMaSV().equalsIgnoreCase(maSV)) {
                    GRADES gr = new GRADES(maSV, Integer.parseInt(ta), Integer.parseInt(th), Integer.parseInt(gdtc));
                    String result = gsv.updateGrade(gr);
                    JOptionPane.showMessageDialog(this, result);
                    flag = 2;
                    break;
                }
            }
        }
        if (flag == 0) {
            JOptionPane.showMessageDialog(this, "Sinh viên không tồn tại");
        }

        loadTable();
        loadSortTable();
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed

        String maNhap = txtTimKiem.getText();
        int count = 0;

        for (GRADES o : listGR) {
            if (o.getMaSV().equalsIgnoreCase(maNhap)) {
                String hoTen = sss.getNameStudent(maNhap);
                lblHoTen.setText(hoTen);
                txtMaSV.setText(listGR.get(count).getMaSV());
                txtTiengAnh.setText(String.valueOf(listGR.get(count).getTiengAnh()));
                txtTinHoc.setText(String.valueOf(listGR.get(count).getTinHoc()));
                txtGDTC.setText(String.valueOf(listGR.get(count).getGdTC()));
                lblDTB.setText(String.valueOf((listGR.get(count).getTiengAnh() + listGR.get(count).getTinHoc() + listGR.get(count).getGdTC()) / 3));
                count = 1;
                break;
            }
            count++;
        }
        if (count != 1) {
            JOptionPane.showMessageDialog(this, "Sinh viên không tồn tại");

        }
    }//GEN-LAST:event_btnSearchActionPerformed

